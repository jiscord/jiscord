package site.lifix.jiscord.api.statics.presence;

public class ActivityActionType {
    // Join a game with the user
    public static final int JOIN = 1;

    // Spectate a game the user is playing
    public static final int SPECTATE = 2;

    // Listen to a song the user is playing on Spotify
    public static final int LISTEN = 3;

    // Join a stream with the user
    public static final int WATCH = 4;

    // Request to join a game with the user
    public static final int JOIN_REQUEST = 5;
}
